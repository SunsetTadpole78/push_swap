#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t			ssrc;
	unsigned int	i;

	ssrc = ft_strlen(src);
	i = 0;
	if (size == 0)
		return (ssrc);
	while (src[i] && i < size - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (ssrc);
}
